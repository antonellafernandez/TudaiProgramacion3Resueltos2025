package Parcial26OCT2024.Ejercicio4;

public class Producto {
    private int p; // Peso
    private int v; // Valor

    public Producto(int p, int v) {
        this.p = p;
        this.v = v;
    }

    public int getP() {
        return p;
    }

    public void setP(int p) {
        this.p = p;
    }

    public int getV() {
        return v;
    }

    public void setV(int v) {
        this.v = v;
    }
}
